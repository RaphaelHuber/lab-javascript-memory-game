let MemoryGame = function (cards) {
  this.cards = cards;
  this.pickedCards = [];
  this.pairsClicked = 0;
  this.pairsGuessed = 0;
};

MemoryGame.prototype.shuffleCards = function () {
  let randomNumb = 0;
  let n = this.cards.length;
  while (n > 0) {
    randomNumb = Math.floor(Math.random() * this.cards.length);
    this.pickedCards.push(this.cards[randomNumb]);
    delete this.cards[randomNumb];
    n--;
  }
};

MemoryGame.prototype.checkIfPair = function (firstCard, secondCard) {
  this.pairsClicked += 1;
  if (firstCard === secondCard) {
    this.pairsGuessed += 1;
    return true;
  } else {
    return false;
  }
}

MemoryGame.prototype.isFinished = function () {
  if (this.pairsGuessed > 11) {
    return true;
    alert("You have won the game, well done!");
  } else {
    return false;
  }
};